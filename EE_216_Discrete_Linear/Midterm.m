T=0.001;
t=0.4:T:1.6;


R = 2*10^3;
C = 10 * 10^-6;
a = R*C;
b = 1;  
c = 1;

y(1)=0;

for n=1:length(t)
        if (n==1)
            y(1) = 0;
        else
            y(n) = (1/(a+b*T))*((a*y(n-1))+c*T*(t==0));
        end
end

plot(y);

x = (t.*cos(30*pi*t+30)+ ...
     8*cos(16*pi*t+10)).*((t-.08>=0) - (t-1.2>=0));
 
plot(t,x);



for n=0:length(t)

        x(n) = (n.*cos(30*pi*n+30)+ ...
                8*cos(16*pi*n+10)).*((n-.08>=0) - (n-1.2>=0));
     
        if (n==0)
            y(n) = 0;
        else
            y(n) = (1/(a+b*T))*((a*y(n-1))+c*T*x(n));
        end
end

plot(y);

